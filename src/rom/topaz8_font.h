/*
 * Get the glyph data for a character
 * Returns pointer to 8 bytes of bitmap data, or NULL if out of range
 */
static inline const UBYTE *topaz8_get_glyph(UBYTE ch)
{
    if (ch < TOPAZ8_FIRST || ch > TOPAZ8_LAST)
    {
        /* Return space for out-of-range characters */
        return g_topaz8_data[0];
    }
    return g_topaz8_data[ch - TOPAZ8_FIRST];
}

